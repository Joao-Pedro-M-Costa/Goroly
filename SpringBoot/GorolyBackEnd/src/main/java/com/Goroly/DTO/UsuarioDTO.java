package com.Goroly.DTO;

import com.Goroly.domain.Usuario;

public class UsuarioDTO {
	private String CPF;
	
	private String username;
	private String password;
    private String email;
    private String telefone1;
    private String telefone2;
    
    
    private String nomerua;
    private String bairro;
    private String cidade;
    private String estado;
    private String CEP;
    
    private String nome;
    private String sobrenome;
    private String nacionalidade;
    private String fusohorario;
    private String idioma;
    private String datanascimento;
    
    UsuarioDTO(Usuario usuario){
    	this.CPF=usuario.getCPF();
    	
    	this.username=usuario.getUsername();
    	this.password=usuario.getPassword();
    	this.email=usuario.getEmail();
    	this.telefone1=usuario.getTelefone1();
    	this.telefone2=usuario.getTelefone2();
        
        
    	this.nomerua=usuario.getNome();
    	this.bairro=usuario.getBairro();
    	this.cidade=usuario.getCidade();
    	this.estado=usuario.getEstado();
    	this.CEP=usuario.getCEP();
        
    	this.nome=usuario.getNome();
    	this.sobrenome=usuario.getSobrenome();
    	this.nacionalidade=usuario.getNacionalidade();
    	this.fusohorario=usuario.getFusohorario();
    	this.idioma=usuario.getIdioma();
    	this.datanascimento=usuario.getDatanascimento();
    	
    }

	public String getCPF() {
		return CPF;
	}

	public void setCPF(String cPF) {
		CPF = cPF;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getTelefone1() {
		return telefone1;
	}

	public void setTelefone1(String telefone1) {
		this.telefone1 = telefone1;
	}

	public String getTelefone2() {
		return telefone2;
	}

	public void setTelefone2(String telefone2) {
		this.telefone2 = telefone2;
	}

	public String getNomerua() {
		return nomerua;
	}

	public void setNomerua(String nomerua) {
		this.nomerua = nomerua;
	}

	public String getBairro() {
		return bairro;
	}

	public void setBairro(String bairro) {
		this.bairro = bairro;
	}

	public String getCidade() {
		return cidade;
	}

	public void setCidade(String cidade) {
		this.cidade = cidade;
	}

	public String getEstado() {
		return estado;
	}

	public void setEstado(String estado) {
		this.estado = estado;
	}

	public String getCEP() {
		return CEP;
	}

	public void setCEP(String cEP) {
		CEP = cEP;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getSobrenome() {
		return sobrenome;
	}

	public void setSobrenome(String sobrenome) {
		this.sobrenome = sobrenome;
	}

	public String getNacionalidade() {
		return nacionalidade;
	}

	public void setNacionalidade(String nacionalidade) {
		this.nacionalidade = nacionalidade;
	}

	public String getFusohorario() {
		return fusohorario;
	}

	public void setFusohorario(String fusohorario) {
		this.fusohorario = fusohorario;
	}

	public String getIdioma() {
		return idioma;
	}

	public void setIdioma(String idioma) {
		this.idioma = idioma;
	}

	public String getDatanascimento() {
		return datanascimento;
	}

	public void setDatanascimento(String datanascimento) {
		this.datanascimento = datanascimento;
	}
}
